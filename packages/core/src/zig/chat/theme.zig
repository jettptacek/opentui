fn hexDigit(comptime c: u8) u8 {
    return switch (c) {
        '0'...'9' => c - '0',
        'a'...'f' => c - 'a' + 10,
        'A'...'F' => c - 'A' + 10,
        else => unreachable,
    };
}

fn hex(comptime s: []const u8) RGBA {
    @setEvalBranchQuota(10000);
    const r = comptime (hexDigit(s[1]) << 4) | hexDigit(s[2]);
    const g = comptime (hexDigit(s[3]) << 4) | hexDigit(s[4]);
    const b = comptime (hexDigit(s[5]) << 4) | hexDigit(s[6]);
    return .{
        @as(f32, @floatFromInt(r)) / 255.0,
        @as(f32, @floatFromInt(g)) / 255.0,
        @as(f32, @floatFromInt(b)) / 255.0,
        1.0,
    };
}

