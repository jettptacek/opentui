pub fn findTheme(id: []const u8) *const Theme {
    for (&themes) |*t| {
        if (std.mem.eql(u8, t.id, id)) return t;
    }
    return &themes[0]; // default to One Dark
}

pub fn getDefaultTheme() *const Theme {
    return &themes[0];
}
